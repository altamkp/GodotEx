namespace GodotEx;

/// <summary>
/// Arbitrary direction enum representing 2-DOF directions.
/// </summary>
public enum Direction2D {
    /// <summary>
    /// Direction pointing to the right.
    /// </summary>
    Right, 
    
    /// <summary>
    /// Direction pointing to the left.
    /// </summary>
    Left, 
    
    /// <summary>
    /// Direction pointing up.
    /// </summary>
    Up, 
    
    /// <summary>
    /// Direction pointing down.
    /// </summary>
    Down, 
}
