namespace GodotEx;

/// <summary>
/// Arbitrary direction enum representing 3-DOF directions.
/// </summary>
public enum Direction {
    /// <summary>
    /// Direction pointing to the right.
    /// </summary>
    Right, 
    
    /// <summary>
    /// Direction pointing to the left.
    /// </summary>
    Left, 
    
    /// <summary>
    /// Direction pointing up.
    /// </summary>
    Up, 
    
    /// <summary>
    /// Direction pointing down.
    /// </summary>
    Down, 
    
    /// <summary>
    /// Direction pointing forward.
    /// </summary>
    Forward, 
    
    /// <summary>
    /// Direction pointing back.
    /// </summary>
    Back
}
